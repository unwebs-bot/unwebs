/**
 * Get selected FAQ items by number.
 *
 * @param array $numbers 배치할 Q 번호 배열 (예: [1, 2, 5, 13, 14])
 * @return array 해당 Q&A 배열
 */
function starter_faq_items($numbers)
{
    $all = starter_faq_all();
    $result = array();
    foreach ($numbers as $n) {
        if (isset($all[$n])) {
            $result[] = $all[$n];
        }
    }
    return $result;
}

